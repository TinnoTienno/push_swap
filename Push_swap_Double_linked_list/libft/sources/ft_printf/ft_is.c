int	ft_isspec(const char c)
{
	if (c == 'c' || c == 's' || c == 'p' || c == 'd' || c == 'i' || c == 'u' || \
		c == 'x' || c == 'X' || c == '%')
		return (1);
	return (0);
}

int	ft_isflag(const char c)
{
	if (c == ' ' || c == '+' || c == '-' || c == '#' || c == '0')
		return (1);
	return (0);
}
